import Foundation
import UIKit

extension UIButton {
    
    func greenButton(){
        self.layer.cornerRadius = 4
        self.backgroundColor = UIColor(displayP3Red: (177.0/255.0), green: (188.0/255.0), blue: (57.0/255.0), alpha: 1)
    }
    
}
